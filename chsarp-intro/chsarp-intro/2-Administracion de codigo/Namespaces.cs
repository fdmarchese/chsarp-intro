    public static class Namespaces
    {
        /*
          Namespaces (espacios de nombres)
          Los namespaces son un concepto organizativo del código complementario a como se administren los archivos en un file system.
          Lo que nos proporcionan es una forma unívoca de identificar un tipo.
          Por ejemplo:
           - si se quiere utilizar acceso a archivos se utilizan las clases dentro del namespace System.IO
           - si se quiere utilizar manipulación de xml se utilizan las clases dentro del namespace System.Xml
           - si se quieren utilizar los métodos de LINQ se importa el namespace System.Linq.

          La forma que tenemos de "importar" dichas librerías en un archivo en c# es a través de la palabra reservada "using" SIEMPRE que sea utilizada de la siguiente manera:
             using System.IO;
             using System.Xml;
             using System.Linq;
          */
    }
}
